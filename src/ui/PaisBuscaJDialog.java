    private void loadRecords() throws SQLException {
         
        String sql = "SELECT sigla AS Sigla, nome AS Nome FROM PAIS WHERE 1 = 1";
        if (!txtNome.getText().isEmpty()){
            sql = sql.concat(" AND nome like '%"+txtNome.getText()+"%'");
        }
        sql = sql.concat(" ORDER BY nome");
        ResultSetTableModel tableModel = new ResultSetTableModel(sql);
        jTablePaises.setModel(tableModel);

         //Adjusting columns 
        jTablePaises.getColumnModel().getColumn(0).setMaxWidth(50); //sigla
        jTablePaises.getColumnModel().getColumn(1).setMinWidth(200);//"nome
        
       
        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
        rightRenderer.setHorizontalAlignment(SwingConstants.LEFT);
        jTablePaises.getColumnModel().getColumn(0).setCellRenderer(rightRenderer);
    }
